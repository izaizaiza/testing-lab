using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace BasicMath
{
    public class BasicMath
    {
        // each method should take in two double arguments and returns
        // the corresponding operation result
        // add method
        public double Add(double x, double y)
        {
            return x + y;
        }

        // subtract method
        public double Subtract(double x, double y)
        {

        return x - y;
        }

        // divide method
        public double Divide(double x, double y)
        {
            return x / y;
        }

        // multiply method
        public double Mutiply(double x, double y)
        {
            return x * y;
        }
    }
}
